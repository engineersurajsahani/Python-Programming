# Math Module

import math

# Example: Using math module functions
# Math Module 

# Example 1: Square root
num = 25
sqrt_result = math.sqrt(num)
print(f"Square root of {num}: {sqrt_result}")

# Example 2: Power
base = 2
exponent = 3
power_result = math.pow(base, exponent)
print(f"{base} raised to the power of {exponent}: {power_result}")

# Example 3: Trigonometric functions
angle_rad = math.radians(45)
sin_result = math.sin(angle_rad)
cos_result = math.cos(angle_rad)
print(f"Sine of 45 degrees: {sin_result}")
print(f"Cosine of 45 degrees: {cos_result}")

# Example 4: Constants
print(f"Value of pi: {math.pi}")
print(f"Euler's number (e): {math.e}")
