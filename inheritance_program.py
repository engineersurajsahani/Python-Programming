# Single Inheritance
class A:
    pass 

class B(A):
    pass 

# Multilevel Inheritance
class A:
    pass 

class B(A):
    pass 

class C(B):
    pass 

# Multiple Inheritance
class A:
    pass 

class B:
    pass 

class C(A,B):
    pass 
 
# Hierarchial Inheritance
class A:
    pass 

class B(A):
    pass 

class C(A):
    pass

# Hybrid Inheritance
class A:
    pass 

class B(A):
    pass 

class C(A):
    pass

class D(B,C):
    pass
